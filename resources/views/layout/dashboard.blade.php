<script>
  // Fungsi untuk mengirim permintaan AJAX dan memperbarui tabel
  function fetchData(sortBy) {
    $.ajax({
      url: "{{ route('getSortedData') }}", // Ganti dengan rute yang sesuai untuk mengambil data yang terurut
      type: "GET",
      data: { sort_by: sortBy },
      success: function (response) {
        // Update tabel dengan respons yang diterima
        $('#table-body').html(response);
      },
      error: function (xhr, status, error) {
        console.log(xhr.responseText); // Tampilkan pesan kesalahan jika permintaan gagal
      }
    });
  }

  // Fungsi untuk menangani perubahan dropdown dan memanggil fetchData dengan opsi yang dipilih
  $(document).ready(function () {
    $('.dropdown-item').click(function (e) {
      e.preventDefault();
      var sortBy = $(this).text().toLowerCase().replace(" ", "_");
      fetchData(sortBy);
    });
  });
</script>

